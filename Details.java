package com.airline;

public class Details {

	private String fromu;
	private String tou;
	public Details(String fromu, String tou) {
		super();
		this.fromu = fromu;
		this.tou = tou;
	}
	
	public String getFromu() {
		return fromu;
	}
	
	public void setFromu(String fromu) {
		this.fromu = fromu;
	}
	
	public String getTou() {
		return tou;
	}
	
	public void setTou(String tou) {
		this.tou = tou;
	}
	
}
